def build_prompt(question: str, sources) -> str:
    material = "\n\n".join(
        f"[{s.source_id}] 标题：{s.document.title}\n更新时间：{s.document.updated_at}\n内容：{s.document.body[:3500]}"
        for s in sources
    )
    return f"问题：{question}\n\n检索材料：\n{material}\n\n仅依据材料回答；不要输出引用列表。"
